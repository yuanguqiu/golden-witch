SET NAMES UTF8;
DROP DATABASE IF EXISTS tedu;
CREATE DATABASE tedu CHARSET=UTF8;
USE tedu;
CREATE TABLE dept(
  did INT PRIMARY KEY,
  dname VARCHAR(15) UNIQUE
);
CREATE TABLE emp(
  eid INT PRIMARY KEY AUTO_INCREMENT,
  ename VARCHAR(15),
  sex BOOL,
  birthday DATE,
  salary DECIMAL(8,2),
  deptID INT,
  FOREIGN KEY(deptID) REFERENCES dept(did)
);
